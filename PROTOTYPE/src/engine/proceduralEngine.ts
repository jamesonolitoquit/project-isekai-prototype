/**
 * Generate a pool of quests from active faction conflicts
 * Called when NPCs need to offer quests
 */
export function generateQuestPoolFromConflicts(
  state: WorldState,
  conflicts: FactionConflict[],
  factions: Faction[],
  count: number = 3
): ProceduralQuest[] {
  const quests: ProceduralQuest[] = [];
  const maxAttempts = count * 3; // Prevent infinite loops
  let attempts = 0;

  while (quests.length < count && attempts < maxAttempts) {
    attempts++;

    // Pick a random active conflict
    const activeConflicts = conflicts.filter(c => c.active);
    if (activeConflicts.length === 0) break;

    const conflict = activeConflicts[Math.floor(random() * activeConflicts.length)];
    
    // Pick one of the faction participants
    const factionId = conflict.factionIds[Math.floor(random() * conflict.factionIds.length)];
    const faction = factions.find(f => f.id === factionId);
    
    if (!faction) continue;

    // Generate quest for this conflict
    const quest = generateProceduralQuest(state, undefined, conflict, faction);
    
    // Check if we already have a very similar quest
    const isDuplicate = quests.some(q => 
      q.title === quest.title || 
      q.themes.some(t => quest.themes.includes(t)) && q.difficulty === quest.difficulty
    );

    if (!isDuplicate) {
      quests.push(quest);
    }
  }

  return quests;
}

/**
 * ALPHA_M14: Generate "Alpha the Bound One" - Rare Emergent Entity
 * 
 * Trigger: High Paradox (> 75) + Corrupted Biome + Night
 * Alpha reads from canonJournal to reference player history
 * She is a legendary entity that observes the player and speaks their personal narrative
 * 
 * @returns Encounter object if Alpha should manifest, null otherwise
 */
export function checkAlphaTheBindOneManifest(state: WorldState): ProceduralEncounter | null {
  // Check trigger conditions
  const paradoxLevel = state.player?.temporalDebt ?? 0;
  if (paradoxLevel <= 75) {
    return null; // Insufficient paradox
  }

  // Check location (corrupted biome)
  const playerLocation = state.locations?.find(l => l.id === state.player.location);
  const isCorrupted = playerLocation?.biome === 'corrupted';
  if (!isCorrupted) {
    return null; // Wrong biome
  }

  // Check time (night only)
  const hour = state.hour ?? 12;
  const isNight = hour >= 20 || hour < 6;
  if (!isNight) {
    return null; // Wrong time
  }

  // All conditions met - Alpha manifests!
  // Retrieve player's most impactful narrative pivots
  const journal = (state as any).canonJournal as { narrativePivots?: any[] } | undefined;
  const pivots = journal?.narrativePivots ?? [];
  const topPivots = pivots.slice(-3); // Last 3 most recent high-impact moments

  // Generate Alpha's dialogue referencing player history
  let alphaDialogue = `*A shimmering figure materializes before you—a woman of impossible grace, her eyes containing infinite reflections.*\n\n"Greetings, Time-Walker. I am Alpha, Bound One of the Paradox Currents.\n\n`;

  if (topPivots.length > 0) {
    alphaDialogue += `I have observed you across the fractured timeline. I remember:\n`;
    topPivots.forEach((pivot: any, idx: number) => {
      alphaDialogue += `\n${idx + 1}. "${pivot.description || 'A moment of significance'}"`;
    });
    alphaDialogue += `\n\nYour choices echo through the fabric of reality itself. The paradox you carry is no accident—it is your own making.\n`;
  } else {
    alphaDialogue += `Your time-debt precedes you. The world bends around your presence.\n`;
  }

  alphaDialogue += `\nWhat will you ask of me, Paradox-Bearer?`;

  return {
    id: `alpha-encounter-${state.tick ?? 0}`,
    type: 'social',
    title: 'Alpha the Bound One',
    description: alphaDialogue,
    difficulty: 8, // High difficulty - she's legendary
    participants: ['alpha-bound-one', 'player'],
    generatedAt: Date.now(),
    location: state.player.location
  };
}

export function validateThemicConsistency(
  quest: ProceduralQuest,
  state: WorldState
): boolean {
  // ALPHA_M3: Basic thematic validation
  // 1. Check if quest themes are recognized
  // 2. Validate difficulty is in range
  // 3. Ensure objectives are non-empty
  
  const validThemes = [
    'exploration', 'combat', 'social', 'mission', 'rescue',
    'investigation', 'espionage', 'delivery', 'protection', 'trade',
    'negotiation', 'diplomacy', 'heist', 'pilgrimage', 'religion',
    'reconnaissance', 'scouting', 'logistics', 'commerce', 'evidence',
    'stealth', 'support', 'defense', 'good', 'evil', 'neutral',
    'power', 'conquest', 'corruption', 'growth', 'healing', 'compassion',
    'mystery', 'investigation'
  ];

  const allThemesValid = quest.themes.every(t => validThemes.includes(t));
  const difficultyValid = quest.difficulty >= 1 && quest.difficulty <= 10;
  const objectivesValid = quest.objectives.length > 0;
  
  return allThemesValid && difficultyValid && objectivesValid;
}

